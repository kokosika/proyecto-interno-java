package com.example.demo.dto.custom;

public class EtapaProyectoDto {

	private Integer etapaProyectoId;
	private String nombre;
	private String descripcion;
	public Integer getEtapaProyectoId() {
		return etapaProyectoId;
	}
	public void setEtapaProyectoId(Integer etapaProyectoId) {
		this.etapaProyectoId = etapaProyectoId;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	
	
}
