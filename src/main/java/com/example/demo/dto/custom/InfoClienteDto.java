package com.example.demo.dto.custom;

public class InfoClienteDto {
	public String nombreProyecto;
	public String nombreCliente;
	public String nombreContraparte;
	public String cargoContraparte;
	public String getNombreProyecto() {
		return nombreProyecto;
	}
	public void setNombreProyecto(String nombreProyecto) {
		this.nombreProyecto = nombreProyecto;
	}
	public String getNombreCliente() {
		return nombreCliente;
	}
	public void setNombreCliente(String nombreCliente) {
		this.nombreCliente = nombreCliente;
	}
	public String getNombreContraparte() {
		return nombreContraparte;
	}
	public void setNombreContraparte(String nombreContraparte) {
		this.nombreContraparte = nombreContraparte;
	}
	public String getCargoContraparte() {
		return cargoContraparte;
	}
	public void setCargoContraparte(String cargoContraparte) {
		this.cargoContraparte = cargoContraparte;
	}
}
